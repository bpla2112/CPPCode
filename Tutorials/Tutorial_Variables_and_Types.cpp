#include <stdio.h>

/*
 * Code teaches how variables are declared.
 * Uses types of int, float, and double.
 * Variable sum is the sum of a, b, and c.
 * Try playing around with this and see what happens when you change the types.
 */
int main() {
    int a = 3;
    float b = 4.5;
    double c = 5.25;

    //calculate the sum and print it
    float sum = a + b + c;
    printf("The sum of a, b, and c is %f.", sum);
}

